#!/bin/bash
set -e

usage() {
  echo "Usage: $0 -h <host> -u <deploy_user> -d <domain>"
  echo
  echo "  -h  Remote host (IP or hostname)"
  echo "  -u  Deploy user (to locate puma.rb under /home/<user>/coredns_ui/)"
  echo "  -d  Domain name for the Caddy virtual host (e.g. ns01.clawstation.ai)"
  exit 1
}

while getopts "h:u:d:" opt; do
  case $opt in
    h) HOST="$OPTARG" ;;
    u) DEPLOY_USER="$OPTARG" ;;
    d) DOMAIN="$OPTARG" ;;
    *) usage ;;
  esac
done

if [[ -z "$HOST" || -z "$DEPLOY_USER" || -z "$DOMAIN" ]]; then
  echo "Error: -h, -u, and -d are required."
  echo
  usage
fi

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# SSH multiplexing — reuse a single TCP connection for all Ansible tasks
export ANSIBLE_SSH_ARGS="-o ControlMaster=auto -o ControlPersist=60s -o ControlPath=/tmp/ansible-ssh-%h-%p-%r -o StrictHostKeyChecking=no"
# Pipelining — send commands over the existing SSH connection instead of creating temp files
export ANSIBLE_PIPELINING=true

ansible-playbook "$SCRIPT_DIR/caddy-production.yml" \
  -i "$HOST," \
  -u root \
  --extra-vars "deploy_user=$DEPLOY_USER domain=$DOMAIN"
